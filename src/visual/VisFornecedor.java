    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (validaCampos()) {
            if (preencherObj()) {
                //lbMenssagem.setText("estou aqui!");
                DAO = new ConFornecedor();

                if (acao == 1) {
                    if (!verificaCnpj(obj)) {
                        try {
                            if (DAO.insert(obj)) {
                                ativaBotao(false);
                                ativaCampos(false);
                                limpaCampos();
                                lbMenssagem.setText("Cadastrado com sucesso!");
                            } else {
                                lbMenssagem.setText("NÃ£o foi possivel inserir");
                            }
                        } catch (Exception e) {
                            lbMenssagem.setText("Erro : " + e.getMessage());
                        }
                    } else {
                        try {
                            retornaObj(DAO.select(obj.getCnpj()));
                            ativaBotao(false);
                        } catch (Exception e) {
                            lbMenssagem.setText("Erro de retorno: " + e.getMessage());
                        }
                    }
                }
                if (acao == 2) {
                    try {
                        if (DAO.update(obj)) {
                            ativaBotao(false);
                            ativaCampos(false);
                            lbMenssagem.setText("Atualizado com sucesso!");
                            try {
                                retornaObj(DAO.select(obj.getCnpj()));
                                preencherObj();
                                ativaBotao(false);
                            } catch (Exception e) {
                                lbMenssagem.setText("Erro de retorno: " + e.getMessage());
                            }

                        } else {
                            lbMenssagem.setText("erro ao atualizar!");

                        }
                    } catch (Exception e) {
                    }

                }

            }
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:

        DAO = new ConFornecedor();
        ArrayList<ModFornecedor> lsFor;
        
        
        DefaultTableModel tb = new DefaultTableModel();
        tbPesquisa.setModel(tb);

        try {
            lsFor = DAO.selectAll();
            
            tb.addColumn("CODIGO");
            tb.addColumn("NOME");
            tb.addColumn("CNPJ");
            tb.addColumn("TELEFONE");
            
            Object[] coluna = new Object[4];
                        
            int lnhs = lsFor.size();
            
            for (int i = 0; i < lnhs; i++) {
                coluna[0] = lsFor.get(i).getId();
                coluna[1] = lsFor.get(i).getNome();
                coluna[2] = lsFor.get(i).getCnpj();
                coluna[3] = lsFor.get(i).getTelefone();
                tb.addRow(coluna);
            }
            
            
        } catch (Exception e) {
            lbMenssagem.setText("Erro: " + e.getMessage());
        }


    }//GEN-LAST:event_btnPesquisarActionPerformed

