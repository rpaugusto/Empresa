    private boolean validaCampos() {
        //metodo responsavel por validar os campos obrigatorios
        if (edtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Nome em branco!");
            edtNome.requestFocus();
            return false;
        }
        if (edtCpf.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo CPF em branco!");
            edtCpf.requestFocus();
            return false;
        }
        if (edtRenda.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Renda em branco!");
            edtRenda.requestFocus();
            return false;
        }
        return true;
    }

    private boolean preencherObj() throws Exception {
        //medoto responsavel por setar os valores do Objeto
        obj = new ModCliente();

        obj.setNome(edtNome.getText());
        obj.setEndereco(edtEndereco.getText());
        obj.setBairro(edtBairro.getText());
        obj.setCidade(edtCidade.getText());
        obj.setTelefone(edtTelefone.getText());
        obj.setCelular(edtCelular.getText());
        obj.setCpf(edtCpf.getText());
        obj.setRg(edtRg.getText());
        obj.setRenda(Double.parseDouble(edtRenda.getText()));

        return true;
    }

