    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            if (validaCampos()) {
                if (preencherObj()) {
                    DAO = new ConCliente();

                    //salvando no banco
                    if (acao.equals("insert")) {
                        if (DAO.insert(obj)) {
                            JOptionPane.showMessageDialog(this, "Gravado com sucesso!", "Cadastro de Clientes", 1);

                            ativarMenu(false);
                            retornaObj(obj);
                            
                        } else {
                            JOptionPane.showMessageDialog(this, "erro, n√£o foi possivel gravar!");
                        }
                    }
                    if (acao.equals("update")) {
                        if (DAO.update(obj)) {
                            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!", "Cadastro de Clientes", 1);

                            ativaCampos(false);
                            ativarMenu(false);
                            limpaCampos();
                        } else {
                            JOptionPane.showMessageDialog(this, "erro, n√£o foi possivel atualizar!");
                        }
                    }

                    ativaCampos(false);
                    ativarMenu(false);
                    limpaCampos();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

